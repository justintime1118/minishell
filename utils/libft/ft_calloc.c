#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	char	*ptr;
	char	*ret;
	size_t	cnt;

	ptr = malloc(count * size);
	if (ptr == NULL)
		return (NULL);
	ret = ptr;
	cnt = count * size;
	while (cnt--)
	{
		*ptr = 0;
		ptr++;
	}
	return (ret);
}
